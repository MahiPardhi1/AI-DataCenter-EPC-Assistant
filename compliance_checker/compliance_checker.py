import os
import re
import gc
import argparse
import pandas as pd

try:
    import pdfplumber
except ImportError:
    pdfplumber = None

try:
    import fitz  # PyMuPDF -- pure pip install, no external binary needed
except ImportError:
    fitz = None

try:
    import easyocr
    # Loaded once and reused across every OCR call -- loading it per-file
    # would re-read the model weights from disk every time, which is slow
    # and, on constrained machines, can exhaust memory.
    _EASYOCR_READER = None
except ImportError:
    easyocr = None
    _EASYOCR_READER = None


def _get_easyocr_reader():
    global _EASYOCR_READER
    if _EASYOCR_READER is None and easyocr is not None:
        print("  [ocr] Loading OCR model (first call only, may take a moment)...")
        _EASYOCR_READER = easyocr.Reader(["en"], gpu=False, verbose=False)
    return _EASYOCR_READER


def extract_pdf_text(pdf_path, ocr_min_chars=50, ocr_dpi=100):
    text = ""
    if pdfplumber is not None:
        try:
            with pdfplumber.open(pdf_path) as pdf:
                pages_text = [p.extract_text() or "" for p in pdf.pages]
                text = "\n".join(pages_text).strip()
        except Exception:
            text = ""

    if len(text) >= ocr_min_chars:
        return text, "text_layer"

    if fitz is None or easyocr is None:
        return text, "text_layer"

    reader = _get_easyocr_reader()
    if reader is None:
        return text, "text_layer"

    ocr_text_parts = []
    try:
        doc = fitz.open(pdf_path)
        zoom = ocr_dpi / 72.0
        mat = fitz.Matrix(zoom, zoom)
        for page in doc:
            pix = page.get_pixmap(matrix=mat)
            img_path = pdf_path + ".__ocr_tmp.png"
            pix.save(img_path)
            del pix
            try:
                result = reader.readtext(img_path, detail=0, paragraph=True)
                ocr_text_parts.append("\n".join(result))
                del result
            finally:
                if os.path.exists(img_path):
                    os.remove(img_path)
            gc.collect()
        doc.close()
    except Exception as e:
        return text, "text_layer"

    ocr_text = "\n".join(ocr_text_parts).strip()
    if len(ocr_text) > len(text):
        return ocr_text, "ocr"
    return text, "text_layer"


def _search(pattern, text, flags=re.IGNORECASE):
    m = re.search(pattern, text, flags)
    return m


def extract_ups_efficiency(text):
    m = _search(r"efficiency of\s*([\d.]+)\s*percent", text)
    if not m:
        m = _search(r"efficiency[^0-9]{0,15}([\d.]+)\s*%", text)
    if m:
        return {"found": True, "value": float(m.group(1)), "raw_evidence": m.group(0)}
    return {"found": False, "value": None, "raw_evidence": ""}


def extract_ups_voltage(text):
    m = _search(r"(\d{3})V,?\s*(\d+)Hz|(\d{3})V.*?3-Phase", text)
    if m or "415V" in text.replace(" ", ""):
        m2 = _search(r"415\s*V", text)
        if m2:
            return {"found": True, "value": "415V 3-Phase 50Hz", "raw_evidence": m2.group(0)}
    return {"found": False, "value": None, "raw_evidence": ""}


def extract_ups_topology(text):
    m = _search(r"tier\s*iii|concurrently maintainable|double conversion", text)
    if m:
        return {"found": True, "value": m.group(0), "raw_evidence": m.group(0)}
    return {"found": False, "value": None, "raw_evidence": ""}


def extract_chiller_draw(text):
    m = _search(r"peak power draw of\s*([\d.]+)\s*kW", text)
    if not m:
        m = _search(r"([\d.]+)\s*kW", text)
    if m:
        return {"found": True, "value": float(m.group(1)), "raw_evidence": m.group(0)}
    return {"found": False, "value": None, "raw_evidence": ""}


def extract_chiller_supply_temp(text):
    m = _search(r"([\d.]+)\s*C\s*design target|target of\s*([\d.]+)\s*C|meets the\s*([\d.]+)\s*C", text)
    if m:
        val = next(g for g in m.groups() if g)
        return {"found": True, "value": float(val), "raw_evidence": m.group(0)}
    return {"found": False, "value": None, "raw_evidence": ""}


def extract_chiller_redundancy(text):
    if re.search(r"n\+1", text, re.IGNORECASE) and "upgrade" not in text.lower():
        return {"found": True, "value": "N+1", "raw_evidence": "N+1 stated"}
    if re.search(r"upgrade module|field-installed upgrade", text, re.IGNORECASE):
        return {"found": True, "value": "N (upgrade available)", "raw_evidence": "N base + chargeable N+1 upgrade"}
    if re.search(r"single n unit|ships as a single n", text, re.IGNORECASE):
        return {"found": True, "value": "N", "raw_evidence": "Single N unit"}
    return {"found": False, "value": None, "raw_evidence": ""}


