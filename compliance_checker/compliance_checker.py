    m = _search(r"CPCB[-\s]?IV\+|emission[s]?\s*(certif\w*|complian\w*)", text)
    if m and re.search(r"CPCB", text, re.IGNORECASE):
        return {"found": True, "value": m.group(0), "raw_evidence": m.group(0)}
