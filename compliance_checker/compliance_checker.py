def classify_batt_autonomy(extracted, client_value):
    mandate = _num(r"([\d.]+)", client_value)
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not state discharge autonomy."
    val = extracted["value"]
    if val >= mandate:
        return "Compliant", f"{val} min", f"Exceeds the {mandate}-minute mandate."
    return "Non-Compliant", f"{val} min", f"Below the {mandate}-minute mandate."


def classify_batt_ambient(extracted, client_value):
    m = re.search(r"([\d.]+)\s*C\s*\+/-\s*([\d.]+)\s*C", client_value)
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not state ambient operating rating."
    lo_v, hi_v = extracted["value"]
    display = f"{lo_v} C to {hi_v} C"
    if m:
        center, tol = float(m.group(1)), float(m.group(2))
        design_lo, design_hi = center - tol, center + tol
        if lo_v == design_lo and hi_v == design_hi:
            return "Compliant", display, f"Vendor's rated range exactly matches the {design_lo}-{design_hi} C design band."
        return "Non-Compliant", display, (
            f"Vendor's rated range ({display}) is wider than and conflicts with the "
            f"client's tighter {design_lo}-{design_hi} C design point; site HVAC control "
            "is designed around the narrower client band, not the vendor's commercial rating."
        )
    return "Partial Compliance", display, "Could not fully verify against client tolerance band."


def classify_crah_humidity(extracted, client_value):
    m = re.search(r"(\d+)%\s*-\s*(\d+)%", client_value)
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not state humidity range."
    lo_v, hi_v = extracted["value"]
    display = f"{lo_v}% - {hi_v}%"
    if m:
        lo_c, hi_c = float(m.group(1)), float(m.group(2))
        if lo_v <= lo_c and hi_v >= hi_c:
            return "Compliant", display, f"Vendor range is a superset of the {lo_c}%-{hi_c}% client spec."
        return "Partial Compliance", display, "Vendor range only partially overlaps the client spec."
    return "Partial Compliance", display, "Could not fully verify against client spec."


def classify_crah_supply_air_temp(extracted, client_value):
    if not extracted["found"]:
        return "Missing Information", None, "Vendor manual is silent on supply air temperature capability."
    return "Compliant", f"{extracted['value']} C", "Matches client setpoint."


