def _num(pattern, s):
    m = re.search(pattern, s)
    return float(m.group(1)) if m else None


def classify_ups_efficiency(extracted, client_value):
    threshold = _num(r"([\d.]+)", client_value)
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not state measured efficiency."
    val = extracted["value"]
    if val >= threshold:
        return "Compliant", f"{val} %", f"Meets or exceeds the {threshold}% mandate."
    return "Non-Compliant", f"{val} %", f"Measured efficiency ({val}%) is below the {threshold}% mandate."


def classify_ups_voltage(extracted, client_value):
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not confirm input voltage/frequency."
    return "Compliant", extracted["value"], "Matches client input power specification."


def classify_ups_topology(extracted, client_value):
    if not extracted["found"]:
        return "Missing Information", None, "Vendor submittal does not confirm topology / Tier III compliance."
    return "Compliant", extracted["value"], "Vendor document references Tier III / concurrently maintainable topology."


def classify_chiller_draw(extracted, client_value):
    threshold = _num(r"([\d.]+)", client_value)
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not state compressor power draw."
    val = extracted["value"]
    if val <= threshold:
        return "Compliant", f"{val} kW", f"Within the {threshold} kW ceiling."
    return "Non-Compliant", f"{val} kW", f"Exceeds the {threshold} kW ceiling by {round(val - threshold, 1)} kW."


def classify_chiller_supply_temp(extracted, client_value):
    target = _num(r"([\d.]+)", client_value)
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not confirm chilled water supply temperature."
    val = extracted["value"]
    if abs(val - target) <= 0.5:
        return "Compliant", f"{val} C", f"Within tolerance of the {target} C design target."
    return "Non-Compliant", f"{val} C", f"Outside tolerance of the {target} C design target."


def classify_chiller_redundancy(extracted, client_value):
    if not extracted["found"]:
        return "Missing Information", None, "Vendor document does not state redundancy configuration."
    val = extracted["value"]
    if val == "N+1":
        return "Compliant", val, "Matches the mandatory N+1 redundancy requirement."
    if "upgrade" in val.lower():
        return "Partial Compliance", val, "Base offer ships as N; N+1 only available as a chargeable field upgrade."
    return "Non-Compliant", val, "Vendor offer does not meet N+1 redundancy requirement."


