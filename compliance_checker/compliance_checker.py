def build_equipment_to_vendor_docs(vendor_map_df, vendor_docs_root):
    mapping = {}
    for _, row in vendor_map_df.iterrows():
        doc_paths = []
        for col in ("Datasheet_Doc", "Quotation_Doc"):
            rel = str(row.get(col, "") or "").strip()
            if rel:
                doc_paths.append(os.path.join(vendor_docs_root, rel))
        mapping[row["Equipment_ID"]] = {
            "Vendor_ID": row["Vendor_ID"],
            "doc_paths": doc_paths,
        }
    return mapping


def run_compliance_check(project_root, out_path):
    kb_root = os.path.join(project_root, "09_Compliance_Knowledge_Base")
    vendor_docs_root = os.path.join(project_root, "02_Vendor_Documents")

    requirements_df = pd.read_csv(os.path.join(kb_root, "requirements_register.csv"))
    remediation_df = pd.read_csv(os.path.join(kb_root, "remediation_knowledge_base.csv"))
    vendor_map_df = pd.read_csv(os.path.join(vendor_docs_root, "vendor_equipment_map.csv"))

    eq_to_docs = build_equipment_to_vendor_docs(vendor_map_df, vendor_docs_root)

    text_cache = {}

    def get_text(path):
        if path not in text_cache:
            if os.path.exists(path):
                text, method = extract_pdf_text(path)
                text_cache[path] = text
                print(f"  [read] {os.path.relpath(path, project_root)}  ({method}, {len(text)} chars)")
            else:
                text_cache[path] = ""
        return text_cache[path]

    print(f"Loaded {len(requirements_df)} requirements from requirements_register.csv")
    print("Extracting vendor document text (OCR runs automatically for scanned PDFs)...")

    rows = []
    for _, req in requirements_df.iterrows():
        req_id = req["Requirement_ID"]
        eq_id = req["Equipment_ID"]
        client_value = req["Client_Value"]

        doc_info = eq_to_docs.get(eq_id, {"Vendor_ID": None, "doc_paths": []})
        vendor_id = doc_info["Vendor_ID"]
        combined_text = "\n".join(get_text(p) for p in doc_info["doc_paths"])

        extractor = EXTRACTORS.get(req_id)
        classifier = CLASSIFIERS.get(req_id)
        if extractor is None or classifier is None:
            label, vendor_value, rationale = "Missing Information", None, "No extraction rule defined for this requirement."
        else:
            extracted = extractor(combined_text)
            label, vendor_value, rationale = classifier(extracted, client_value)

        remediation_row = remediation_df[remediation_df["Requirement_ID"] == req_id]
        suggested_action = ""
        if label != "Compliant" and not remediation_row.empty:
            suggested_action = remediation_row.iloc[0]["Suggested_Action"]

        rows.append({
            "Requirement_ID": req_id,
            "Category": req["Category"],
            "Equipment_ID": eq_id,
            "Vendor_ID": vendor_id,
            "Requirement_Text": req["Requirement_Text"],
            "Client_Value": client_value,
            "Vendor_Value": vendor_value,
            "Compliance_Label": label,
            "Rationale": rationale,
            "Criticality": req["Criticality"],
            "Impact": req["Impact"],
            "Probability": req["Probability"],
            "Risk_Score": req["Risk_Score"],
            "Suggested_Action": suggested_action,
        })

    report_df = pd.DataFrame(rows).sort_values("Risk_Score", ascending=False)
    report_df.to_csv(out_path, index=False)

    print(f"\nCompliance report written to: {out_path}")
    print("\nLabel distribution:")
    print(report_df["Compliance_Label"].value_counts().to_string())

    print("\nTop risk items requiring attention (Non-Compliant / Partial / Missing):")
    flagged = report_df[report_df["Compliance_Label"] != "Compliant"]
    for _, r in flagged.iterrows():
        print(f"  [{r['Compliance_Label']}] {r['Requirement_ID']} ({r['Equipment_ID']}, risk={r['Risk_Score']}): {r['Rationale']}")

    gt_path = os.path.join(kb_root, "compliance_ground_truth.csv")
    if os.path.exists(gt_path):
        gt_df = pd.read_csv(gt_path)
        evaluate_against_ground_truth(report_df, gt_df, out_path)

    return report_df


def evaluate_against_ground_truth(report_df, gt_df, out_path):
    merged = report_df.merge(
        gt_df[["Requirement_ID", "Compliance_Label"]],
        on="Requirement_ID", suffixes=("_pred", "_true")
    )
    labels = sorted(set(merged["Compliance_Label_true"]) | set(merged["Compliance_Label_pred"]))

    correct = (merged["Compliance_Label_pred"] == merged["Compliance_Label_true"]).sum()
    total = len(merged)
    print(f"\n=== Evaluation vs ground truth ({total} labeled requirements) ===")
    print(f"Overall accuracy: {correct}/{total} ({100*correct/total:.1f}%)")

    conf = pd.crosstab(
        merged["Compliance_Label_true"], merged["Compliance_Label_pred"],
        rownames=["True"], colnames=["Predicted"], dropna=False
    ).reindex(index=labels, columns=labels, fill_value=0)
    print("\nConfusion matrix:")
    print(conf.to_string())

    metrics_rows = []
    for label in labels:
        tp = ((merged["Compliance_Label_pred"] == label) & (merged["Compliance_Label_true"] == label)).sum()
        fp = ((merged["Compliance_Label_pred"] == label) & (merged["Compliance_Label_true"] != label)).sum()
        fn = ((merged["Compliance_Label_pred"] != label) & (merged["Compliance_Label_true"] == label)).sum()
        support = (merged["Compliance_Label_true"] == label).sum()
        precision = tp / (tp + fp) if (tp + fp) > 0 else float("nan")
        recall = tp / (tp + fn) if (tp + fn) > 0 else float("nan")
        f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) > 0 else float("nan")
        metrics_rows.append({
            "Label": label, "Support": support,
            "Precision": round(precision, 3) if precision == precision else precision,
            "Recall": round(recall, 3) if recall == recall else recall,
            "F1": round(f1, 3) if f1 == f1 else f1,
        })
    metrics_df = pd.DataFrame(metrics_rows)
    print("\nPer-label precision / recall / F1:")
    print(metrics_df.to_string(index=False))

    macro_f1 = metrics_df["F1"].dropna().mean()
    print(f"\nMacro-averaged F1: {macro_f1:.3f}")

    mismatches = merged[merged["Compliance_Label_pred"] != merged["Compliance_Label_true"]]
    if not mismatches.empty:
        print("\nMismatches (worth reviewing your extraction rules for these):")
        for _, r in mismatches.iterrows():
            print(f"  {r['Requirement_ID']}: predicted={r['Compliance_Label_pred']} vs true={r['Compliance_Label_true']}")
    else:
        print("\nNo mismatches -- every requirement matched its ground-truth label.")

    metrics_out_path = os.path.splitext(out_path)[0] + "_eval_metrics.csv"
    metrics_df.to_csv(metrics_out_path, index=False)
    conf_out_path = os.path.splitext(out_path)[0] + "_confusion_matrix.csv"
    conf.to_csv(conf_out_path)
    print(f"\nSaved per-label metrics to: {metrics_out_path}")
    print(f"Saved confusion matrix to: {conf_out_path}")


def main():
    parser = argparse.ArgumentParser(description="AI Compliance Checker for EPC project documents")
    parser.add_argument("--project-root", default="project_data", help="Path to the project_data folder")
    parser.add_argument("--out", default="compliance_report.csv", help="Output CSV path for the compliance report")
    args = parser.parse_args()

    run_compliance_check(args.project_root, args.out)


if __name__ == "__main__":
    main()